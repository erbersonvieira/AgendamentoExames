    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Fachada f = new Fachada();
        ArrayList<Examinador> list = new ArrayList<Examinador>();
        list = f.listagemGeralExaminador();
        this.tabela();
        for (int i=0;i < list.size() ; i++){
         modelo.addRow(new Object[] {list.get(i).getIdExaminador(), list.get(i).getNomeExaminador(),
                                     list.get(i).getCpfExaminador()});
        }
    }//GEN-LAST:event_jButton2ActionPerformed
     public void tabela(){
        modelo = new DefaultTableModel(); 
        jTable1.setModel(modelo);
        modelo.addColumn("Matricula");
        modelo.addColumn("Nome");
        modelo.addColumn("C.P.F.");
    }

