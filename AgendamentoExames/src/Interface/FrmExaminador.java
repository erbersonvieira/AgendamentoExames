    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        Fachada f = new Fachada();
        int valor = 0;
        Examinador exam = new Examinador();
        //populate(material, rs);
        exam.setIdExaminador(Integer.parseInt(jTextField1.getText()));
        exam.setNomeExaminador(jTextField2.getText());
        exam.setCpfExaminador(jTextField3.getText());
        

       valor = f.inserirExaminador(exam);

       if (valor > 0) {
           JOptionPane.showMessageDialog(null, "Dados Gravados com Sucesso!!!");
       } else {
           JOptionPane.showMessageDialog(null, "Erro na Gravação !!!");
       }
                 
    }//GEN-LAST:event_jButton1ActionPerformed

